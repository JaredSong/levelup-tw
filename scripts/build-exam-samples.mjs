// Generates src/app/generatedExamSamples.ts: for each published exam, the list of
// work-item (section) titles and a few text-only sample questions. The per-exam
// SEO pages (ExamPage) render these so each exam has unique, keyword-rich,
// crawlable content that matches "<考科> 題庫" search intent — without the page
// needing to fetch the full (large) question bank at runtime.
import { readFileSync, readdirSync, writeFileSync } from 'node:fs'

const EXAMS_DIR = new URL('../public/data/exams/', import.meta.url)
const OUT = new URL('../src/app/generatedExamSamples.ts', import.meta.url)
const SAMPLES_PER_EXAM = 4

function asArray(bank) {
  if (Array.isArray(bank)) return bank
  return bank.questions ?? Object.values(bank).find(Array.isArray) ?? []
}

/** Text-only, single-answer, fully-formed questions make the cleanest samples. */
function isCleanText(q) {
  return !q.hasFigure && q.kind === 'single' && q.prompt && Array.isArray(q.options)
    && q.options.length >= 2 && Array.isArray(q.answers) && q.answers.length === 1
}

// Spread the samples across different work items rather than taking the first N
// from one section, so the page previews the breadth of the exam.
function pickSamples(clean) {
  const bySection = new Map()
  for (const q of clean) {
    if (!bySection.has(q.sectionTitle)) bySection.set(q.sectionTitle, q)
  }
  const spread = [...bySection.values()]
  const chosen = spread.slice(0, SAMPLES_PER_EXAM)
  for (const q of clean) {
    if (chosen.length >= SAMPLES_PER_EXAM) break
    if (!chosen.includes(q)) chosen.push(q)
  }
  return chosen.map((q) => ({
    id: q.id,
    sectionTitle: q.sectionTitle ?? '',
    prompt: q.prompt,
    options: q.options,
    answer: q.answers[0],
  }))
}

const out = {}
for (const dir of readdirSync(EXAMS_DIR).sort()) {
  let bank
  try {
    bank = JSON.parse(readFileSync(new URL(`${dir}/questions.json`, EXAMS_DIR), 'utf8'))
  } catch {
    continue
  }
  const arr = asArray(bank)
  const clean = arr.filter(isCleanText)
  const sections = [...new Set(arr.map((q) => q.sectionTitle).filter(Boolean))]
  out[dir] = { sections, samples: pickSamples(clean) }
}

const header = `// Generated by scripts/build-exam-samples.mjs. Do not edit by hand.
export interface ExamSample {
  id: string
  sectionTitle: string
  prompt: string
  options: string[]
  /** 1-based index of the correct option. */
  answer: number
}
export interface ExamSampleSet {
  sections: string[]
  samples: ExamSample[]
}
export const GENERATED_EXAM_SAMPLES: Record<string, ExamSampleSet> = `
writeFileSync(OUT, `${header}${JSON.stringify(out, null, 2)}\n`)
console.log(`build-exam-samples: wrote ${Object.keys(out).length} exams to src/app/generatedExamSamples.ts`)
